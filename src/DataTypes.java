public class DataTypes {
    public static void main(String[] args) {
        // string - sir de caractere delimitate de ""
        String marca = "Dacia";
        String model = "Spring";

        // integer - numar intreg
        int an_fabricatie = 2022;

        // double - numar zecinal

        double pret = 8550.25;

        // boolean - adevarat/fals

        boolean inmatriculata = true;

        // char - un singur caracter delimitat de ''

        char nota = 'A';

        // scrie cu litere mari
        System.out.println(marca.toUpperCase());
        // dimensiunea stringului

        System.out.println(marca.length());


    }
}

